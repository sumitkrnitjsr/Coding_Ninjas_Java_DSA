package Object_Oriented_Programming.Generics;

public class Normal_Pair {

	private int first;
	private int second;
	
	public Normal_Pair(int first, int second) {
		this.first = first;
		this.second = second;
	}
	
	
	public int getFirst() {
		return first;
	}
	
	public int getSecond() {
		return second;
	}
	
	public void setFirst(int first) {
		this.first = first;
	}
	
	public void getSecond(int second) {
		this.second = second;
	}
	
	
}
