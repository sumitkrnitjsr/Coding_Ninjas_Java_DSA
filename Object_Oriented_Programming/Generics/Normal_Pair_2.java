package Object_Oriented_Programming.Generics;

public class Normal_Pair_2 {
	
	private String first;
	private String second;
	
	public Normal_Pair_2(String first, String second) {
		this.first = first;
		this.second = second;
	}
	
	
	public String getFirst() {
		return first;
	}
	
	public String getSecond() {
		return second;
	}
	
	public void setFirst(String first) {
		this.first = first;
	}
	
	public void getSecond(String second) {
		this.second = second;
	}
	

}
